<h1>About</h1>
<hr>
<p>
Every day, test your tactical prowess by guessing the randomly generated Rainbow Six Siege operator on R6dle. A challenging and fun twist for fans of the strategic, team-based shooter game, R6dle offers a unique daily puzzle to keep your operator knowledge sharp.
</p>
<p>This project was heavily inspired by <a href="https://loldle.net">LoLdle</a>, <a href="https://www.nytimes.com/games/wordle/index.html">Wordle</a>, and other -dle games!</p>
<p>
  The operator updates daily at 00:00 UCT time.
</p>
